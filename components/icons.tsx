export function getFileIcon(fileName: string): React.ReactElement {
    const extension = fileName.split('.').pop()?.toLowerCase();
    switch (extension) {
        case 'doc':
        case 'docx':
            return <FileIcon className="w-6 h-6 text-blue-600" />;
        case 'xls':
        case 'xlsx':
            return <FileIcon className="w-6 h-6 text-green-600" />;
        case 'ppt':
        case 'pptx':
            return <FileIcon className="w-6 h-6 text-orange-500" />;
        case 'pdf':
            return <FileIcon className="w-6 h-6 text-red-500" />;
        case 'png':
        case 'jpg':
        case 'jpeg':
        case 'gif':
        case 'svg':
            return <FileIcon className="w-6 h-6 text-purple-500" />;
        default:
            return <FileIcon />;
    }
}
